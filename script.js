const pluse1 = document.getElementById("pluse");
const minus1 = document.getElementById("minus");
const para1 = document.getElementById("para");

pluse1.addEventListener('click', () => {

    para1.style.display = 'block';
    pluse1.style.visibility = 'hidden';
    pluse1.style.display = 'none';
    minus1.style.visibility = 'visible';
    minus1.style.display = 'block';

});

minus1.addEventListener('click', () => {

    para1.style.display = 'none';
    minus1.style.visibility = 'hidden';
    pluse1.style.visibility = 'visible';
    minus1.style.display = 'none';
    pluse1.style.display = 'block';

});



